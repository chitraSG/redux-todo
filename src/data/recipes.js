function recipeMatchesFilters(recipe, filters) {
  const { diet, allergies, cuisines, goals } = filters;

  if (diet.size) {
    const ok = [...diet].every((key) => {
      if (key === "veggies") return recipe.isVegetarian;
      if (key === "dairy") return recipe.hasDairy;
      return false;
    });
    if (!ok) return false;
  }

  if (allergies.has("gluten") && !recipe.isGlutenFree) return false;
  if (allergies.has("nuts") && !recipe.isNutFree) return false;

  if (cuisines.size) {
    const has = [...cuisines].some((c) => recipe.cuisines.includes(c));
    if (!has) return false;
  }

  if (goals.size) {
    const ok = [...goals].some((g) => recipe.goals.includes(g));
    if (!ok) return false;
  }

  return true;
}

export function filterRecipes(recipes, filters) {
  return recipes.filter((r) => recipeMatchesFilters(r, filters));
}
